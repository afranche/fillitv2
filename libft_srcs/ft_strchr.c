#include <string.h>
#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i] != '\0' && s[i] != (char)c)
		i++;
	if (s[i] == (char)c || (s[i] == '\0' && (char)c == '\0'))
		return (char*)(s + i);
	return (NULL);
}
